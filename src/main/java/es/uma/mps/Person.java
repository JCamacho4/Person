package es.uma.mps;

public class Person {

    private final String name;
    private final int age;
    private final String gender; // Male, Female

    public Person(String name, int age, String gender) {
        this.name = name;
        this.age = age;
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getGender() {
        return gender;
    }
}
